/**
 * add - sums two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: computation result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subs the second integer from the first
 * @a: first integer
 * @b: second integer
 *
 * Return: computation result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mod - calculates the first integer modulo the second
 * @a: first integer
 * @b: second integer
 *
 * Return: computation result
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * div - divises the first integer by the second
 * @a: first integer
 * @b: second integer
 *
 * Return: computation result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: computation result
 */
int mul(int a, int b)
{
	return (a * b);
}
